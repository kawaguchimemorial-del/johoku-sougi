// 補助関数
export function getColumn(slug: string): Column | undefined {
  return columns.find((c) => c.slug === slug);
}

export function getCategory(slug: string): ColumnCategory | undefined {
  return columnCategories.find((c) => c.slug === slug);
}

export function getCategoryName(slug: string): string {
  return getCategory(slug)?.name ?? "コラム";
}

// すべてのキーワードを重複なく集めて出現数とともに返す（絞り込みボタン用）
export function allKeywords(): { keyword: string; count: number }[] {
  const map = new Map<string, number>();
  for (const c of columns) {
    for (const k of c.keywords) {
      map.set(k, (map.get(k) ?? 0) + 1);
    }
  }
  return [...map.entries()]
    .map(([keyword, count]) => ({ keyword, count }))
    .sort((a, b) => b.count - a.count);
}
